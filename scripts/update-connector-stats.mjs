#!/usr/bin/env node
/**
 * Count API endpoints across all OpenAPI specs in the connector-gen catalog
 * and write the totals to packages/connector-gen/src/stats.ts.
 *
 * Run: node scripts/update-connector-stats.mjs
 */

import { readFileSync, writeFileSync } from "node:fs"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"
import { parse as parseYaml } from "yaml"

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..")
const CATALOG_SRC = join(ROOT, "packages/connector-gen/src/catalog.ts")
const STATS_OUT = join(ROOT, "packages/connector-gen/src/stats.ts")

const HTTP_METHODS = ["get", "post", "put", "patch", "delete", "head", "options"]
const FETCH_TIMEOUT_MS = 30_000

// ── Extract URLs from catalog source ──────────────────────────────────────────

const catalogSrc = readFileSync(CATALOG_SRC, "utf8")
const entries = [
	...catalogSrc.matchAll(/\{\s*\n(?:(?!^\s*\{)[\s\S])*?url:\s*"([^"]+)"[\s\S]*?\}/gm),
]
	.map((m) => m[1])
	.filter(Boolean)

// Simpler fallback: just grab every url: "..." line
const urls = [...catalogSrc.matchAll(/\burl:\s*"([^"]+)"/g)].map((m) => m[1]).filter(Boolean)

const serviceCount = urls.length
console.log(`Found ${serviceCount} catalog entries`)

// ── Fetch and count ───────────────────────────────────────────────────────────

let totalEndpoints = 0
let fetched = 0
let skipped = 0

for (const url of urls) {
	let spec
	try {
		const controller = new AbortController()
		const timer = setTimeout(() => controller.abort(), FETCH_TIMEOUT_MS)
		const res = await fetch(url, { signal: controller.signal })
		clearTimeout(timer)

		if (!res.ok) {
			console.warn(`  SKIP ${url} — HTTP ${res.status}`)
			skipped++
			continue
		}

		const text = await res.text()
		spec = url.endsWith(".yaml") || url.endsWith(".yml") ? parseYaml(text) : JSON.parse(text)
	} catch (err) {
		console.warn(`  SKIP ${url} — ${err instanceof Error ? err.message : String(err)}`)
		skipped++
		continue
	}

	const paths = spec?.paths ?? {}
	let count = 0
	for (const pathItem of Object.values(paths)) {
		if (!pathItem || typeof pathItem !== "object") continue
		for (const method of HTTP_METHODS) {
			if (method in pathItem) count++
		}
	}

	totalEndpoints += count
	fetched++
	console.log(`  ✓ ${url.split("/").pop()} — ${count} endpoints`)
}

console.log(`\nFetched ${fetched}/${serviceCount} specs (${skipped} skipped)`)
console.log(`Total endpoints: ${totalEndpoints}`)

// ── Write stats ───────────────────────────────────────────────────────────────

const output = `\
// Auto-generated by scripts/update-connector-stats.mjs — do not edit manually.
export const CONNECTOR_STATS = {
\tservices: ${serviceCount},
\tendpoints: ${totalEndpoints},
} as const
`

writeFileSync(STATS_OUT, output, "utf8")
console.log(`\n✓ Wrote ${STATS_OUT}`)
